package poo4;

/**
 *
 * @author PAVILION
 */
public class Rectangulo {
    private int alto;
    private int base;

    public Rectangulo() {
    }

    public Rectangulo(int alto, int base) {
        this.alto = alto;
        this.base = base;
    }

    public int getAlto() {
        return alto;
    }

    public void setAlto(int alto) {
        this.alto = alto;
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }
}
